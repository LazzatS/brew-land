import UIKit

enum BLColor {
    enum Background {
        static let primary = UIColor.systemBackground
    }
    
    enum Button {
        static let active = UIColor.systemBlue
    }
    
    enum Label {
        static let primary = UIColor.label
        static let secondary = UIColor.secondaryLabel
        static let tertiary = UIColor.tertiaryLabel
    }
}
